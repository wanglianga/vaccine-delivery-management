export function generateTempCurve(baseTemp: number, hours: number, variance: number): TemperatureRecord[] {
  const records: TemperatureRecord[] = []
  const startTime = new Date('2024-07-01T08:00:00')
  for (let i = 0; i < hours * 4; i++) {
    const time = new Date(startTime.getTime() + i * 15 * 60 * 1000)
    const temp = baseTemp + (Math.random() - 0.5) * variance
    records.push({
      time: time.toISOString(),
      temperature: Math.round(temp * 10) / 10,
    })
  }
  return records
}
